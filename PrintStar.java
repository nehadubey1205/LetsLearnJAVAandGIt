public class PrintStar {
    public static void main(String[]args){
        //Scanner sc = new Scanner(System.in);
        int n ;// rows
        int noofStar;// star in per row
        for(n=1; n<=10;n++){
            for(noofStar =1; noofStar<=n;noofStar++){
                System.out.print("*");
            }System.out.println();// this is for line change
            //System.out.println()
        }
    
    }
    
}
